# 1)Addation function 

Addation <- function(value1, value2){
  func <- value1 + value2
  return(func)
}


# 2)Subtraction function

Subtraction <- function(value1, value2){
  func <- value1 - value2
  return(func)
}


# 3)Multiplication function

Multiplication <- function(value1, value2){
  func <- value1 * value2
  return(func)
}


# 4)Division function

Division <- function(value1, value2){
  func <- value1 / value2
  return(func)
}


# 5)Exponetial function

Exponetial <- function(value1, value2){
  func <- value1 ** value2
  return(func)
}


# 6)squareroot function

Squareroot <- function(value){
  func <- value ** (1.0/2.0)
  return(func)
}


# 7)squared function

Squared <- function(value){
  func <- value * value
  return(func)
}


# 8)squareroot function

cuberoot <- function(value){
